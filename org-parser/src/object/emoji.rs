#[derive(Debug, Clone, Copy)]
pub struct Emoji<'a> {
    pub name: &'a str,
    pub mapped_item: char,
}

impl<'a> Parseable<'a> for Emoji<'a> {
    fn parse(
        parser: &mut Parser<'a>,
        mut cursor: Cursor<'a>,
        parent: Option<NodeID>,
        parse_opts: ParseOpts,
    ) -> Result<NodeID> {
        let start = cursor.index;
        // skip past colon
        cursor.next();
        if cursor.curr() == COLON {
            return Err(MatchError::InvalidLogic);
        }

        let ret_match = cursor.fn_until(|chr: u8| chr.is_ascii_whitespace() || chr == COLON)?;

        match cursor[ret_match.end] {
            COLON => {
                if let Ok(moji) = parse_emoji(ret_match.obj) {
                    Ok(parser.alloc(moji, start, ret_match.end + 1, parent))
                } else {
                    Err(MatchError::InvalidLogic)
                }
            }
            _ => Err(MatchError::InvalidLogic),
        }
    }
}

pub(crate) fn parse_emoji(name: &str) -> Result<Emoji> {
    if let Some(mapped_item) = EMOJI_MAP.get(name) {
        Ok(Emoji {
            name,
            mapped_item: *mapped_item,
        })
    } else {
        Err(MatchError::InvalidLogic)
    }
}

